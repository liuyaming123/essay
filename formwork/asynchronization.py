conn = aiohttp.TCPConnector(ssl=False)  # 防止ssl报错


async def get_data(i):
    try:
        url = 'https://wxapp.mama.cn/Mamayunqi/Hospital/detail/?' \
              'id=%s&latitude=31.276291&longitude=121.533674' % i

        async with aiohttp.request('GET', url, connector=conn) as resp:
            print(resp.status)
            return await resp.json()
    except Exception as e:
        print(e)


async def gd(i):
    res = await get_data(i)
    print(i, res.get('data').get('hospital'))
    return res


